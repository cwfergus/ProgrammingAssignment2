## These functions enable you to get the inverse of matrix's, without repeating already
## inveresed matrices. Instead matrices are stored in a cache, and that cache is checked 
## before the inverse is found.

## This function creates a special matrix, that can cache its inverse

makeCacheMatrix <- function(x = matrix()) {
    m <- NULL
    set <- function(y) {
        x <<- y
        m <<- NULL
    }
    get <- function() x
    setinverse <- function(solve) m <<- solve
    getinverse <- function() m
    list(set = set, get = get,
         setinverse = setinverse,
         getinverse = getinverse)
}


## The function generates the inverse of the special matrix, if it hasn't been calculated
## if it has, then the cached value is returned.

cacheSolve <- function(x, ...) {
    m <- x$getinverse()
    if(!is.null(m)) {
        message("getting cached data")
        return(m)
    }
    data <- x$get()
    m <- solve(data, ...)
    x$setinverse(m)
    m
}